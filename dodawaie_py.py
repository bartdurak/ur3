" przykład if else pythona"
wybor = input("  + dodawanie , - odejmowanie, * mnożenie, / dzielenie ")

a = int(input())
b = int(input())

if (wybor == '+'):
    print(a + b)
elif (wybor == '-'):
    print(a-b)
elif (wybor == '*'):
    print( a*b)
elif (wybor == '/'):
    print( a/b)
else:
    print("a jest równe b: ")

